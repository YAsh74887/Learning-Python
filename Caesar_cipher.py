def encode (plain_text,shift_amount):
  cipher = " "
  for letters in plain_text:
    position = alphabet.index(letters)
    new_position = position + shift_amount
    new_letter = alphabet[new_position]
    cipher += new_letter
  print(cipher)
  
  
  
def decode(encrypt,shift_amount):
  decode = " "
  for letters in encrypt:
    position = alphabet.index(letters)
    new_position = position - shift_amount
    new_letters = alphabet[new_position]
    decode += new_letters
  print(decode)   
    
   
